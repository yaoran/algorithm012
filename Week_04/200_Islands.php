class Solution
{
    private $dx = [-1, 1, 0, 0];
    private $dy = [0, 0, -1, 1];

    private $g = [];

    /**
     * @param String[][] $grid
     * @return Integer
     */
    function numIslands($grid)
    {
        $isLands = 0;
        $this->g = $grid;
        $m = count($this->g);
        $n = count($this->g[0]);
        for ($i = 0; $i < $m; $i++) {
            for ($j = 0; $j < $n; $j++) {
                if (!$this->g[$i][$j]) {
                    continue;
                }

                $isLands += $this->sink($i, $j);
            }
        }

        return $isLands;
    }

    function sink($i, $j)
    {
        if (!$this->g[$i][$j]) {
            return 0;
        }

        $this->g[$i][$j] = '0';
        $m = count($this->g);
        $n = count($this->g[0]);

        $dcount = count($this->dx);

        // æŠŠä¸Šä¸‹å·¦å³éƒ½æ²‰åº•ã€‚å››è”é€š
        for ($k = 0; $k < $dcount; $k++) {
            $x = $i + $this->dx[$k];
            $y = $j + $this->dy[$k];

            if ($x >= 0 && $x < $m && $y >= 0 && $y < $n) {

                if (!$this->g[$x][$y]) {
                    continue;
                }

                $this->sink($x, $y);
            }
        }

        return 1;
    }
}
